  protected void showInference(String inferenceTime) {
    //if current speed is greater than limit speed...
    //this runs if detected sign contains "speed"...
    if(!(MultiBoxTracker.DetectedS1.isEmpty())) {
      if (MultiBoxTracker.DetectedS1.contains("Speed")&& (MultiBoxTracker.LimitSpeedS1!=0)) {
        if (((currspeed*(3.6)) >= MultiBoxTracker.LimitSpeedS1)&&(Ontracking==false)) {

          //Alert Dialog for Excess Speed
          Ontracking=true;
          play_media();
          AlertDialog.Builder builder = new AlertDialog.Builder(this);
          builder.setIcon(R.drawable.ic_dialog_alert);
          builder.setTitle("You are exceeding the speed limit!");
          builder.setMessage("Speed Limit is " + (int) MultiBoxTracker.LimitSpeedS1 + ",So please Slow down...");
          builder.setCancelable(true);
          builder.setPositiveButton("Ok", new DialogInterface.OnClickListener() {
              @Override
              public void onClick(DialogInterface dialog, int which) {
                  stop_playing();
              }
          });
          final AlertDialog dlg = builder.create();
          dlg.show();
          final Timer t = new Timer();
          t.schedule(new TimerTask() {
            public void run() {
              Ontracking=false;
              stop_playing();
              noofspeedwarnings=noofspeedwarnings+1;
              dlg.dismiss(); // when the task active then close the dialog
              t.cancel(); // also just top the timer thread, otherwise, you may receive a crash report
            }
          }, 20000);
          //
        }
      }
    }
    S1.setText(MultiBoxTracker.DetectedS1);
    inferenceTimeTextView.setText(inferenceTime);
  }

